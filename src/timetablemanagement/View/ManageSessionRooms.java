    private void getsessionDetail(){
        int i=0;
        try {
           
            PreparedStatement st=con.prepareStatement("SELECT * FROM `sessiondetails`");
           
            ResultSet rs=st.executeQuery();
            session.addItem(null);
            while(rs.next()){
                  id[i]=rs.getInt(1);
                  session.addItem(rs.getString(1)+"--"+rs.getString(2)+"--"+rs.getString(4)+"--"+rs.getString(5)+"--"+rs.getString(6)); 
                  i++;
            }
          
          
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }
    private void getroomDetail(){
        int i=0;
        try {
            
            PreparedStatement st1=con.prepareStatement("SELECT * FROM `locationdetails`");
           
            ResultSet rs1=st1.executeQuery();
            room.addItem(null);
            while(rs1.next()){
                  rid[i]=rs1.getInt(1);
                  room.addItem(rs1.getString(3)); 
                   i++;
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }

