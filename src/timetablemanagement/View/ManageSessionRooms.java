    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
             
              try {
               if(session.getSelectedIndex()>0 && room.getSelectedIndex()>0){
            PreparedStatement st=con.prepareStatement("INSERT INTO `sessionrooms`(`ID`, `session`, `room`) VALUES (?,?,?)");
           
            st.setInt(1,0 );
            st.setInt(2,id[session.getSelectedIndex()-1]);
            st.setInt(3,rid[room.getSelectedIndex()-1]);
           
           
            int count=st.executeUpdate();
            if(count>0){
               JOptionPane.showMessageDialog(null, "Room added");
               new AddLocations().setVisible(true);
            }
               }else{
                   JOptionPane.showMessageDialog(null, "please insert details");
               }
             
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "error");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

