    private void ManageLOCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ManageLOCMouseClicked
        
        
         
        DefaultTableModel df=(DefaultTableModel)ManageLOC.getModel();
     int SelectedIndex=ManageLOC.getSelectedRow();
     
     String ID=df.getValueAt(SelectedIndex,0).toString();
     
     try {
            PreparedStatement st1=con.prepareStatement("SELECT `buildingname`, `roomname`, `roomtype`, `capacity` FROM `locationdetails`  WHERE ID="+ID+"");
            
            
            ResultSet rs1=st1.executeQuery();
            while(rs1.next()){
                txtLBuildingName.setText(rs1.getString(1));
                txtlRoomName.setText(rs1.getString(2));
                String type=rs1.getString(3);
                if(type.equals("Lecture Hall")){
                    btnLecHall.setSelected(true);
                }else{
                    btnLab.setSelected(true);
                }
                
               
                txtCapacity.setText(rs1.getString(4));
            
              
                
            }
            
          

             
        } catch (Exception e) {
             e.printStackTrace();
             
        }
    }//GEN-LAST:event_ManageLOCMouseClicked

