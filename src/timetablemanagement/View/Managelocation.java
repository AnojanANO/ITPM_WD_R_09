    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        btnLecHall.setActionCommand("Lecture Hall");
        btnLab.setActionCommand("Laboratory");
        
        DefaultTableModel df=(DefaultTableModel)ManageLOC.getModel();
        int SelectedIndex=ManageLOC.getSelectedRow();
     
        String id=df.getValueAt(SelectedIndex,0).toString();
        try {
            PreparedStatement st=con.prepareStatement("UPDATE `locationdetails` SET `buildingname`=?,`roomname`=?,`roomtype`=?,`capacity`=? WHERE ID="+id+"");
             st.setString(1, txtLBuildingName.getText());
            st.setString(2, txtlRoomName.getText());
            st.setString(3,buttonGroup1.getSelection().getActionCommand());
            st.setString(4, txtCapacity.getText());
            if(txtLBuildingName.getText().isEmpty()|| txtlRoomName.getText().isEmpty()||txtCapacity.getText().toString().isEmpty()||buttonGroup1.getSelection().getActionCommand().isEmpty()){
                 JOptionPane.showMessageDialog(null, " please Enter the all colums");
                 throw new Exception();
            }
             int count=st.executeUpdate();
            if(count>0){
               JOptionPane.showMessageDialog(null, "Succesfully updated");
               table_update();
            
               Clear();
            }
                    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

