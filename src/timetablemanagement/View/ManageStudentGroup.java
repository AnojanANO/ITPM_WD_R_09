    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       DefaultTableModel df=(DefaultTableModel)StudentGroupt.getModel();
        int SelectedIndex=StudentGroupt.getSelectedRow();
     
        String id=df.getValueAt(SelectedIndex,0).toString();
        
        try {
            PreparedStatement st1=con.prepareStatement("DELETE FROM `student details` WHERE ID="+id+"");
             int count=st1.executeUpdate();
            if(count>0){
               JOptionPane.showMessageDialog(null, "Succesfully deleted");
               table_update();
               Clear();
            }
                    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void StudentGrouptMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_StudentGrouptMouseClicked
        DefaultTableModel df=(DefaultTableModel)StudentGroupt.getModel();
     int SelectedIndex=StudentGroupt.getSelectedRow();
     
     String ID=df.getValueAt(SelectedIndex,0).toString();
     
     try {
            PreparedStatement st1=con.prepareStatement("SELECT `Groupid`, `subgroupid`, `academicyearsemester`, `programme`, `Groupnumber`, `subgroupnumber` FROM `student details` WHERE ID="+ID+"");
            
            
            ResultSet rs1=st1.executeQuery();
            while(rs1.next()){
                
                GroupID.setText(rs1.getString(1));
                SubGID.setText(rs1.getString(2));
                AcadamicYear.setText(rs1.getString(3));
                
                String programme=rs1.getString(4);
                switch(programme){
                 case "IT":
                     Programme.setSelectedIndex(0);
                     break;
                 case "CSSE":
                     Programme.setSelectedIndex(1);
                     break;
                 case "CSE":
                     Programme.setSelectedIndex(2);
                     break;
                 case "IM":
                     Programme.setSelectedIndex(3);
                     break;
                 
                }
                
          
                Gnum.setValue(Integer.valueOf(rs1.getString(5)));
                subGnum.setValue(Integer.valueOf(rs1.getString(6)));

                
            }
            
          

             
        } catch (Exception e) {
             e.printStackTrace();
             
        }
    }//GEN-LAST:event_StudentGrouptMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel df=(DefaultTableModel)StudentGroupt.getModel();
        int SelectedIndex=StudentGroupt.getSelectedRow();
     
        String id=df.getValueAt(SelectedIndex,0).toString();
        try {
            PreparedStatement st=con.prepareStatement("UPDATE `student details` SET `Groupid`=?,`subgroupid`=?,`academicyearsemester`=?,`programme`=?,`Groupnumber`=?,`subgroupnumber`=? WHERE ID="+id+"");
            st.setString(1,GroupID.getText());
            st.setString(2, SubGID.getText());
            st.setString(3, AcadamicYear.getText());
            st.setString(4, Programme.getSelectedItem().toString());
            String a=Gnum.getValue().toString();
            st.setString(5, a);
            String b=subGnum.getValue().toString();
            st.setString(6, b);
             int count=st.executeUpdate();
            if(count>0){
               JOptionPane.showMessageDialog(null, "Succesfully updated");
               table_update();
               Clear();
            }
                    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

